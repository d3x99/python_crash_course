#locations i want to visit
locations = ['bali', 'south america', 'north america', 'japan', 'moon']
print(locations)
#temporary sorting
print(sorted(locations))
print(locations)
#reversing
locations.reverse()
print(locations)
#changing to original
locations.reverse()
print(locations)
#sorting permamently alphabetical
locations.sort()
print(locations)
#sorting permamently reverse alphabetical
locations.sort(reverse=True)
print(locations)

